struct timer_entity {
    /* 定时器触发方式 */
    enum timer_mode mode;
    /* 定时器创建的消息处理实体 */
    HWINDOW hwnd;
    /* 定时器间隔 */
    os_u32 elapse;
    /* 已经等待时间 */
    os_u32 current_time;
    /* 回调函数地址 */
    TIMER_FUNC_PTR timer_func;
    /* 事件id */
    os_u32 event_id;
};

